const mongoose = require("mongoose");

var Schema = new mongoose.Schema({
  Firstname: {
    type: String,
    required: true,
  },
  Lastname: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
});
const UserDB = mongoose.model("userdb", Schema);

moudule.exports = UserDB;
